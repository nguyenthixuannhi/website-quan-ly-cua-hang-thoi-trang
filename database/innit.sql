) ENGINE=InnoDB;

CREATE TABLE CHITIETPHIEUNHAP (
    id_chi_tiet_nhap INT NOT NULL,
    id_don_nhap INT,
    id_bien_the INT,
    so_luong INT NOT NULL,
    PRIMARY KEY (id_chi_tiet_nhap)
