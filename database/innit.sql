) ENGINE=InnoDB;

CREATE TABLE LICHSUKHO (
    id_lich_su INT NOT NULL,
    id_bien_the INT,
    id_nguoi_dung INT,
    loai_thao_tac VARCHAR(20),
    so_luong_thay_doi INT,
