CREATE TABLE TRAHANG (
    id_phieu_tra INT NOT NULL,
    id_don_hang INT,
    ly_do VARCHAR(500),
    PRIMARY KEY (id_phieu_tra),
    UNIQUE (id_don_hang)
) ENGINE=InnoDB;

CREATE TABLE CHITIETTRAHANG (
    id_chi_tiet_tra INT NOT NULL,
    id_phieu_tra INT,
    id_bien_the INT,
    so_luong INT,
    PRIMARY KEY (id_chi_tiet_tra)
) ENGINE=InnoDB;

CREATE TABLE DIACHI (
    id_dia_chi INT NOT NULL,
    id_nguoi_dung INT,
